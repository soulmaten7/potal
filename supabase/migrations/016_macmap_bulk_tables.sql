-- Function: lookup_duty_rate_v2()
-- Hierarchical duty rate lookup with prefix matching
-- ============================================================
CREATE OR REPLACE FUNCTION lookup_duty_rate_v2(
  p_reporter CHAR(2),
  p_partner CHAR(2),
  p_product_code VARCHAR(12)
) RETURNS TABLE (
  product_code VARCHAR(12),
  av_duty NUMERIC,
  nav_duty TEXT,
  nav_flag SMALLINT,
  source TEXT,
  match_type TEXT
) LANGUAGE plpgsql AS $$
BEGIN
  -- 1. Try exact match in macmap_min_rates
  RETURN QUERY
    SELECT m.product_code, m.av_duty, NULL::TEXT, NULL::SMALLINT,
           'macmap_min_rates'::TEXT, 'exact_min'::TEXT
    FROM macmap_min_rates m
    WHERE m.reporter_iso2 = p_reporter
      AND m.partner_iso2 = p_partner
      AND m.product_code = p_product_code
    LIMIT 1;
  IF FOUND THEN RETURN; END IF;

  -- 2. Try prefix match (longest first: 8, then 6)
  RETURN QUERY
    SELECT m.product_code, m.av_duty, NULL::TEXT, NULL::SMALLINT,
           'macmap_min_rates'::TEXT, 'prefix_min'::TEXT
    FROM macmap_min_rates m
    WHERE m.reporter_iso2 = p_reporter
      AND m.partner_iso2 = p_partner
      AND (m.product_code = LEFT(p_product_code, 8)
           OR m.product_code = LEFT(p_product_code, 6))
    ORDER BY LENGTH(m.product_code) DESC
    LIMIT 1;
  IF FOUND THEN RETURN; END IF;

  -- 3. Fallback to macmap_ntlc_rates (MFN)
  RETURN QUERY
    SELECT n.hs_code, n.mfn_rate, n.nav_duty_text, NULL::SMALLINT,
           'macmap_ntlc_rates'::TEXT, 'mfn_fallback'::TEXT
    FROM macmap_ntlc_rates n
    WHERE n.destination_country = p_reporter
      AND (n.hs_code = p_product_code
           OR n.hs_code = LEFT(p_product_code, 8)
           OR n.hs_code = LEFT(p_product_code, 6))
    ORDER BY LENGTH(n.hs_code) DESC
    LIMIT 1;
  IF FOUND THEN RETURN; END IF;

  -- 4. Final fallback to duty_rates_live (WITS HS6)
  RETURN QUERY
    SELECT d.hs_code, d.mfn_rate, d.specific_rate_info, NULL::SMALLINT,
           d.source_api, 'wits_hs6'::TEXT
    FROM duty_rates_live d
    WHERE d.destination_country = p_reporter
      AND d.hs_code = LEFT(p_product_code, 6)
      AND d.invalidated_at IS NULL
    LIMIT 1;
END;
$$;

