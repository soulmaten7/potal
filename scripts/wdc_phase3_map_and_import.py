def log(msg):
    ts = time.strftime("%Y-%m-%d %H:%M:%S")
    line = f"[{ts}] {msg}"
    print(line)
    with open(LOG_FILE, "a") as f:
        f.write(line + "\n")

def run_sql(query, retries=3):
    """Execute SQL via Supabase Management API."""
    for attempt in range(retries):
        try:
            result = subprocess.run(
                ["curl", "-s", "-X", "POST",
                 f"https://api.supabase.com/v1/projects/{SUPABASE_PROJECT}/database/query",
                 "-H", f"Authorization: Bearer {SUPABASE_TOKEN}",
                 "-H", "Content-Type: application/json",
                 "-d", json.dumps({"query": query})],
                capture_output=True, text=True, timeout=120
            )
            response = result.stdout
            if '"message"' in response and 'ERROR' in response:
                if attempt < retries - 1:
                    log(f"  SQL error (attempt {attempt+1}): {response[:200]}")
                    time.sleep(2)
                    continue
                return None, response
            try:
                data = json.loads(response)
                return data, None
            except json.JSONDecodeError:
                if attempt < retries - 1:
                    time.sleep(2)
                    continue
                return None, f"JSON decode error: {response[:200]}"
        except subprocess.TimeoutExpired:
            if attempt < retries - 1:
                log(f"  Timeout (attempt {attempt+1}), retrying...")
                time.sleep(5)
                continue
            return None, "Timeout"
        except Exception as e:
            if attempt < retries - 1:
                time.sleep(2)
                continue
            return None, str(e)
    return None, "Max retries exceeded"

