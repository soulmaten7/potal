def main():
    log("=" * 60)
    log("WDC Phase 3 Wave 3: Product name patterns")
    log("=" * 60)

    data, err = run_sql("SELECT product_name FROM product_hs_mappings;")
    if err:
        log(f"Error: {err}"); return
    existing = {r['product_name'].lower().strip() for r in data}
    log(f"Existing mappings: {len(existing)}")

    vdata, _ = run_sql("SELECT product_name FROM hs_classification_vectors;")
    existing_vec = {r['product_name'].lower().strip() for r in vdata} if vdata else set()
    log(f"Existing vectors: {len(existing_vec)}")

    new_m = [(pn, hs6) for pn, hs6 in WAVE3_MAP.items() if pn.lower().strip() not in existing]
    new_v = [(pn, hs6) for pn, hs6 in WAVE3_MAP.items() if pn.lower().strip() not in existing_vec]
    log(f"New mappings: {len(new_m)}")
    log(f"New vectors: {len(new_v)}")

    batch_size = 50
    inserted = 0
    for i in range(0, len(new_m), batch_size):
        batch = new_m[i:i+batch_size]
        values = []
        for pn, hs6 in batch:
            pn_esc = pn.replace("'", "''")
            values.append(f"('{pn_esc}', '{pn_esc}', '{hs6}', 0.92, 'wdc_phase3_pattern', '{{}}'::jsonb, NOW())")
        sql = f"INSERT INTO product_hs_mappings (product_name, category, hs6, confidence, source, metadata, created_at) VALUES {','.join(values)};"
        result, err = run_sql(sql)
        if err:
            for pn, hs6 in batch:
                pn_esc = pn.replace("'", "''")
                r, e = run_sql(f"INSERT INTO product_hs_mappings (product_name, category, hs6, confidence, source, metadata, created_at) VALUES ('{pn_esc}', '{pn_esc}', '{hs6}', 0.92, 'wdc_phase3_pattern', '{{}}'::jsonb, NOW());")
                if not e: inserted += 1
        else:
            inserted += len(batch)
        if (i // batch_size + 1) % 10 == 0:
            log(f"  Mappings progress: {inserted}/{len(new_m)}")
    log(f"Mappings: {inserted} inserted")

    vec_inserted = 0
    for i in range(0, len(new_v), batch_size):
        batch = new_v[i:i+batch_size]
        values = []
        for pn, hs6 in batch:
            pn_esc = pn.replace("'", "''")
            values.append(f"('{pn_esc}', '{pn_esc}', '{hs6}', 'wdc_phase3_pattern', 0.92, NOW(), NOW())")
        sql = f"INSERT INTO hs_classification_vectors (product_name, category, hs_code, source, confidence, created_at, updated_at) VALUES {','.join(values)};"
        result, err = run_sql(sql)
        if err:
            for pn, hs6 in batch:
                pn_esc = pn.replace("'", "''")
                r, e = run_sql(f"INSERT INTO hs_classification_vectors (product_name, category, hs_code, source, confidence, created_at, updated_at) VALUES ('{pn_esc}', '{pn_esc}', '{hs6}', 'wdc_phase3_pattern', 0.92, NOW(), NOW());")
                if not e: vec_inserted += 1
        else:
            vec_inserted += len(batch)
        if (i // batch_size + 1) % 10 == 0:
            log(f"  Vectors progress: {vec_inserted}/{len(new_v)}")
    log(f"Vectors: {vec_inserted} inserted")

    cnt1, _ = run_sql("SELECT COUNT(*) as cnt FROM product_hs_mappings;")
    cnt2, _ = run_sql("SELECT COUNT(*) as cnt FROM hs_classification_vectors;")
    log(f"Final product_hs_mappings: {cnt1[0]['cnt'] if cnt1 else '?'}")
    log(f"Final hs_classification_vectors: {cnt2[0]['cnt'] if cnt2 else '?'}")

    # Source breakdown
    src, _ = run_sql("SELECT source, COUNT(*) as cnt FROM product_hs_mappings GROUP BY source ORDER BY cnt DESC;")
    if src:
        log("Source breakdown (product_hs_mappings):")
        for r in src:
            log(f"  {r['source']}: {r['cnt']}")

    log("Wave 3 complete!")

if __name__ == "__main__":
    main()
